package Tarea;

public class caja {

    public int vertice, peso;

    public caja(int vertice, int peso){
        this.vertice =  vertice;
        this.peso = peso;
    }

    public int getVertice() {
        return vertice;
    }

    public void setVertice(int vertice) {
        this.vertice = vertice;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }
}
